#include <stdio.h>
int main()
{
    int i = 0, j = 1, k = 123;  /*定义三个整型变量，定义变量的同时赋值*/
    int s;
    char a, b, c;               /*定义三个字符型变量*/
    s = j;                      /*将变量j的值赋值给s*/
    a = 'a';                    /*将字符a赋值给变量a*/
    b = a;                      /*把变量a的值赋值给b*/
    c = b;                      /*把变量b的值赋值给c*/
}
